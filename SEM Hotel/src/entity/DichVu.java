package entity;

/**
 *
 * @author Tien Nguyen
 */
public class DichVu {
    private int id;
    private String name;
    private int money;
    private int status;
    private String note;

    public DichVu() {
    }

    public DichVu(int id, String name, int money, int status, String note) {
        this.id = id;
        this.name = name;
        this.money = money;
        this.status = status;
        this.note = note;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
    
    
}
