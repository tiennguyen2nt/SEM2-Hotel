    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (thoihan != -1) {
                String maPhong;
                long ngayNhan = 0;
                String sNgay = tfNgay.getText().trim();
                String TenKH = tfTen.getText().trim();

                String CMND = tfCmnd.getText().trim();
                String SDT = tfDT.getText().trim();
                long ngayTra = 0;

                try {
                    maPhong = tfPhong.getText();
                    if (!maPhong.equals("")) {
                        if (!TenKH.equals("")) {
                            if (!CMND.equals("") && v.checkNumber(CMND)) {
                                try {

                                    if (this.NgayTra == 0 && this.NgayNhan == 0) {
                                        ngayNhan = getLDate();
                                        if (!sNgay.equals("")) {
                                            int Ngay = 0;
                                            try {

                                                Ngay = Integer.parseInt(sNgay);

                                                long lngay = Ngay * 24 * 60 * 60 * 1000;
                                                ngayTra = ngayNhan + lngay;
                                                if (Ngay > thoihan && thoihan != 0) {
                                                    JOptionPane.showMessageDialog(this, "Chỉ có thể đặt nhỏ hơn " + thoihan + " ngày!", "Lỗi thời hạn!", JOptionPane.ERROR_MESSAGE);
                                                } else {
                                                    //Gọi hàm để đặt phòng
                                                    wel.f.setDateHoaDon(TenKH, CMND, SDT, maPhong, ngayNhan, ngayTra);
                                                    JOptionPane.showMessageDialog(this, "Đặt phòng " + maPhong + " thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                                                    CardLayout c = (CardLayout) pn.getLayout();
                                                    tfPhong1.setText(tfPhong.getText().trim());
                                                    tfName.setText(tfTen.getText().trim());
                                                    tfCM.setText(tfCmnd.getText().trim());
                                                    c.show(pn, "themdichvu");
                                                }
                                            } catch (NumberFormatException | HeadlessException e) {
                                                JOptionPane.showMessageDialog(this, "Ngày nhập phải là số!", "Lỗi định dạnh số!", JOptionPane.ERROR_MESSAGE);
                                                System.out.println(e.toString());
                                            }

                                        } else {
                                            JOptionPane.showMessageDialog(this, "Ngày nhập không được bỏ trống!", "Lỗi định dạnh số!", JOptionPane.ERROR_MESSAGE);
                                        }
                                    } else {
                                        ngayTra = this.NgayTra;
                                        ngayNhan = this.NgayNhan;
                                        //Gọi hàm để đặt phòng
                                        wel.f.setDateHoaDon(TenKH, CMND, SDT, maPhong, ngayNhan, ngayTra);
                                        JOptionPane.showMessageDialog(this, "Đặt phòng " + maPhong + " thành công!");
                                        this.NgayNhan = 0;
                                        this.NgayTra = 0;
                                        CardLayout c = (CardLayout) pn.getLayout();

                                        tfPhong1.setText(tfPhong.getText().trim());
                                        tfName.setText(tfTen.getText().trim());
                                        tfCM.setText(tfCmnd.getText().trim());
                                        c.show(pn, "themdichvu");

                                    }

                                } catch (Exception e) {

                                    JOptionPane.showMessageDialog(this, "Ngày nhận trống hoặc không đúng định dạng ngày!", "Dữ liệu trống!", JOptionPane.ERROR_MESSAGE);
                                }
                            } else {
                                JOptionPane.showMessageDialog(this, "CMND trống hoặc không đúng định dạng!", "Lỗi!", JOptionPane.ERROR_MESSAGE);
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Tên khách hàng không được bỏ trống!", "Dữ liệu trống!", JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Số phòng không được bỏ trống!", "Lỗi định dạnh số!", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Mã phòng trống hoặc không phải là số!", "Lỗi!", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Phòng đã có người đặt trước trong 24h tới!", "Thông báo!", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

