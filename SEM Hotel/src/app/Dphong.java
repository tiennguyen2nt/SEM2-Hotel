    public void getPhong(String phong, long ngayNhan, long Ngaytra) {
        tfPhong.setText(phong);
        tfPhong.setEditable(false);
        pn.updateUI();
        if (ngayNhan == 0 || Ngaytra == 0) {
            tfNgayTra.setVisible(false);
            lbNgayTra.setVisible(false);
            thoihan = f.timeFree(phong);
            if (thoihan != 0) {
                lbEndDAy.setText("Phòng " + phong + " còng trống " + thoihan + " ngày.");
            } else {
                lbEndDAy.setVisible(false);
            }
            if (thoihan == -1) {
                tfNgay.setEditable(false);
                lbEndDAy.setText("Phòng " + phong + " đã có người đặt.");
            }
        } else {
            lbtraphong2.setVisible(false);
            tfNgay.setVisible(false);
            lb1.setVisible(false);
            tfNgayNhan.setText(getDate(ngayNhan));
            tfNgayTra.setText(getDate(Ngaytra));
            this.NgayNhan = ngayNhan;
            this.NgayTra = Ngaytra;
            lbEndDAy.setVisible(false);
        }

    }

    public String getDate(long date) {
//        Date d = new Date();
        SimpleDateFormat dinhDangThoiGian = new SimpleDateFormat("dd/MM/yyyy");

        return dinhDangThoiGian.format(date);
    }

