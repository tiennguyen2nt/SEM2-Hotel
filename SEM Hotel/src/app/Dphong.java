    private String getDate() {
        Date d = new Date();
        SimpleDateFormat dinhDangThoiGian = new SimpleDateFormat("HH:mm dd/MM/yyyy");
        String t1 = dinhDangThoiGian.format(d.getTime());

        return t1;
    }

